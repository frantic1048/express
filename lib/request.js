/**
 * 返回 请求的 header
 *
 * `Referrer` header 域是特殊的写法,
 * `Referrer` `Referer` 可互换使用
 *
 * 例子:
 *
 *     req.get('Content-Type');
 *     // => "text/plain"
 *
 *     req.get('content-type');
 *     // => "text/plain"
 *
 *     req.get('Something');
 *     // => undefined
 *
 * 别名是 `req.header()`.
 *
 * @param {String} name
 * @return {String}
 * @public
 */

req.get =
req.header = function header(name) {
  var lc = name.toLowerCase();

  switch (lc) {
    case 'referer':
    case 'referrer':
      return this.headers.referrer
        || this.headers.referer;
    default:
      return this.headers[lc];
  }
};

/**
 * 检查给定的 `type(s)` 是否可接受,
 * 是的话返回最佳匹配,
 * 否则返回 `undefined` 且应当响应 HTTP 406 "Not Acceptable".
 *
 * `type` 值可以是一个 MIME type 字符串
 * 例如 "application/json", 或者是扩展名
 * 例如 "json", 或者是一个逗号分隔的列表 "json, html, text/plain",
 * 参数表是这样 `"json", "html", "text/plain"`,
 * 或者是个数组 `["json", "html", "text/plain"]`. 当给定一个列表
 * 或者数组的时候返回最佳匹配
 *
 * 例如:
 *
 *     // Accept: text/html
 *     req.accepts('html');
 *     // => "html"
 *
 *     // Accept: text/*, application/json
 *     req.accepts('html');
 *     // => "html"
 *     req.accepts('text/html');
 *     // => "text/html"
 *     req.accepts('json, text');
 *     // => "json"
 *     req.accepts('application/json');
 *     // => "application/json"
 *
 *     // Accept: text/*, application/json
 *     req.accepts('image/png');
 *     req.accepts('png');
 *     // => undefined
 *
 *     // Accept: text/*;q=.5, application/json
 *     req.accepts(['html', 'json']);
 *     req.accepts('html', 'json');
 *     req.accepts('html, json');
 *     // => "json"
 *
 * @param {String|Array} type(s)
 * @return {String|Array|Boolean}
 * @public
 */

req.accepts = function(){
  var accept = accepts(this);
  return accept.types.apply(accept, arguments);
};

